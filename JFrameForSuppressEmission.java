    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // bouton : supprimer.
        String emSuppr = jLabel5.getText();
        
        if(emSuppr == null){
            JOptionPane.showMessageDialog(null, "Aucun nom de fichier n'est fournit. Merci de cliquer sur le bouton 'Afficher'.", "Message d'avertissement", JOptionPane.ERROR_MESSAGE);
        }else{
            EmissionManager emsupprmanager = new EmissionManager(null);
            emsupprmanager.suppressAllEmissionElement(emSuppr);
        }
        
        if(emSuppr != null){
            JOptionPane.showMessageDialog(null, "La suppression de l'élément "+ jLabel5.getText() + " a bien eu lieu.");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

