    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // bouton : rechercher.
        jComboBox1.removeAllItems();
        jLabel5.setText(null);
        
        String emissionsearch = jTextField1.getText();
        
        if(emissionsearch.isEmpty()){
            JOptionPane.showMessageDialog(null, "il n'y a aucune expression-clée saisie.", "Message d'avertissement", JOptionPane.ERROR_MESSAGE);
        }else{
           EmissionManager emissionmanager = new EmissionManager(null);
            List<ArrayList<String>> resultat = emissionmanager.extractEmissionFileNames(emissionsearch);
        
            if(resultat.isEmpty()){
                JOptionPane.showMessageDialog(null, "l'expression-clée ne correspond à aucun élément de la base.", "Message d'avertissement", JOptionPane.ERROR_MESSAGE);
            }else{
                for(int i=0; i<resultat.get(0).size(); i++){
                    this.jComboBox1.addItem(resultat.get(0).get(i));                
                }
            } 
        } 
        
        
                       
    }//GEN-LAST:event_jButton1ActionPerformed

